// Boolean - evaluates TRUE or FALSE
/*  The most common types of boolean expressions are those that us relational 
    operators. General syntax of a conditional statement (specifically boolean)
    <expression> <relational operator> <expression>
        (The two expressions MUST match in datatype!!!!)
*/

/*  Boolean operators - take boolean expressions as operands
    AND (&&)
    OR (||)
    NOT (!) - negates the value of the orginal boolean expression

    SYNTAX:
    (b1)&&(b2)
    (b1) || (b2)
    !(b1)
*/
